/**
 * gen-root-dts.mjs — Generate simple re-export .d.ts files for the root confused-ai package.
 *
 * Since @confused-ai/* are published as separate packages and declared as dependencies,
 * the root .d.ts files can simply re-export from them. TypeScript resolves these
 * correctly when @confused-ai/* are installed in node_modules.
 */

import { writeFileSync, mkdirSync, readFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const root = resolve(__dirname, '..');

// Map: [output path, source entry file to read the re-exports from]
// For entries that simply re-export from a @confused-ai/* package, we generate
// "export * from '@confused-ai/pkg'".
// For entries that have own source code in src/, we copy the tsc-generated .d.ts
// which is already built by packages/*/dist.

// Read the src entry file and detect the @confused-ai/* target
function detectReExport(srcFile) {
  try {
    const content = readFileSync(resolve(root, srcFile), 'utf8');
    // Find: export * from '@confused-ai/xxx'
    const match = content.match(/export \* from ['"](@confused-ai\/[^'"]+)['"]/);
    if (match) return { type: 'star', pkg: match[1] };
    // Find: export { ... } from '@confused-ai/xxx'
    const match2 = content.match(/from ['"](@confused-ai\/[^'"]+)['"]/);
    if (match2) return { type: 'mixed', pkg: match2[1] };
  } catch {}
  return null;
}

