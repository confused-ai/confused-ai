/**
 * Code Review Pipeline — bare() + compose() + pipe()
 *
 * Real-world problem: Your team wants automated first-pass code review on every PR.
 * You need three specialized agents chained together:
 *
 *   1. DiffAnalyser  — parse the diff, identify changed files, classify risk level
 *   2. SecurityReviewer — scan for vulnerabilities, secrets, injection risks
 *   3. ReportWriter  — synthesize findings into a GitHub review comment (markdown)
 *
 * This example shows three composition styles:
 *   A. compose(a, b, c) — simple sequential pipeline
 *   B. pipe(a).then(b).then(c).run(prompt) — stepwise builder
 *   C. compose with transform + when options — conditional hand-off
 *
 * What it demonstrates:
 *   ✓  bare()   — zero-defaults agent with explicit LLM and custom hooks
 *   ✓  compose() — two-agent pipeline with optional transform
 *   ✓  pipe()   — chainable multi-agent builder
 *   ✓  Lifecycle hooks: beforeRun, afterRun, buildSystemPrompt
 *   ✓  MockLLMProvider — deterministic output, no API key needed
 *   ✓  Pattern to swap in real LLM for production
 *
 * Run: bun examples/code-review-pipeline.ts
 */

import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { config } from 'dotenv';

config({
    path: path.join(path.dirname(fileURLToPath(import.meta.url)), '.env'),
    quiet: true,
});

import { bare, compose, pipe } from '../src/dx/index.js';
import { MockLLMProvider } from '../src/testing/mock-llm.js';

// ── Mock LLMs (deterministic canned responses) ────────────────────────────

