// ── Helpers ───────────────────────────────────────────────────────────────

function divider(title: string) {
    console.log(`\n${'='.repeat(12)} ${title} ${'='.repeat(12)}\n`);
}

// ── Approach A: compose() — simple sequential pipeline ────────────────────

async function approachA() {
    divider('Approach A: compose()');

    const diffAnalyser = bare({
        name: 'DiffAnalyser',
        instructions: 'Analyse a git diff. Identify changed files, risk level, and a one-paragraph summary.',
        llm: diffLlm,
        hooks: {
            afterRun: (result) => {
                console.log('[DiffAnalyser] completed — risk level extracted');
                return result;
            },
        },
    });

    const reportWriter = bare({
        name: 'ReportWriter',
        instructions: 'You receive a security analysis. Write a structured GitHub PR review comment in Markdown.',
        llm: reportLlm,
    });

    // SecurityReviewer between them
    const securityReviewer = bare({
        name: 'SecurityReviewer',
        instructions: 'You receive a diff analysis. Scan for security issues: SQL injection, secrets, auth flaws.',
        llm: securityLlm,
    });

    // compose(a, b, c) — chains three agents sequentially
    const pipeline = compose(diffAnalyser, securityReviewer, reportWriter);

    const result = await pipeline.run(
        `Please review this PR diff:\n\n${PR_DIFF}`,
        { onChunk: (text) => process.stdout.write(text) },
    );

    console.log('\n\n--- Final report ---\n');
    console.log(result.text);
    console.log(`\nSteps: ${result.steps}, Finish: ${result.finishReason}`);
}

// ── Approach B: pipe() — stepwise builder ─────────────────────────────────

async function approachB() {
    divider('Approach B: pipe().then().then().run()');

    const step1 = bare({ name: 'DiffAnalyser', instructions: 'Analyse the diff.', llm: diffLlm });
    const step2 = bare({ name: 'SecurityReviewer', instructions: 'Review security.', llm: securityLlm });
    const step3 = bare({ name: 'ReportWriter', instructions: 'Write the review comment.', llm: reportLlm });

    const result = await pipe(step1)
        .then(step2)
        .then(step3)
        .run(`Review this diff:\n\n${PR_DIFF}`);

    console.log('Pipeline result (last step):\n');
    console.log(result.text);
}

// ── Approach C: compose with when + transform ─────────────────────────────

async function approachC() {
    divider('Approach C: compose() with conditional hand-off');

    const highRiskLlm = new MockLLMProvider({
        response: '## Diff Analysis\n**Risk level:** HIGH\nSQL injection detected. Sending to security review.',
    });
    const lowRiskLlm = new MockLLMProvider({
        response: '## Diff Analysis\n**Risk level:** LOW\nNo critical changes. Minor style issues only.',
    });

    // Only escalate to SecurityReviewer when risk is HIGH
    const diffHighRisk = bare({ name: 'DiffAnalyser', instructions: 'Analyse the diff.', llm: highRiskLlm });
    const diffLowRisk  = bare({ name: 'DiffAnalyser', instructions: 'Analyse the diff.', llm: lowRiskLlm });
    const secReviewer  = bare({ name: 'SecurityReviewer', instructions: 'Security review.', llm: securityLlm });

    console.log('--- High-risk diff (escalates to security reviewer) ---\n');
    const highRiskPipeline = compose(diffHighRisk, secReviewer, {
        when: (result) => result.text.includes('HIGH'),
        transform: (result) => `Security review requested.\n\nContext:\n${result.text}`,
    });
    const r1 = await highRiskPipeline.run(`Review this diff:\n\n${PR_DIFF}`);
    console.log(r1.text);

    console.log('\n--- Low-risk diff (stops after diff analysis) ---\n');
    const lowRiskPipeline = compose(diffLowRisk, secReviewer, {
        when: (result) => result.text.includes('HIGH'),
    });
    const r2 = await lowRiskPipeline.run('Review this tiny whitespace-only diff.');
    console.log(r2.text);
    console.log('(Security reviewer skipped — risk was LOW)');
}

// ── Main ──────────────────────────────────────────────────────────────────

async function main() {
    console.log('confused-ai — Code Review Pipeline: bare() + compose() + pipe()\n');
    console.log('PR diff:\n' + PR_DIFF + '\n');

    await approachA();
    await approachB();
    await approachC();

    divider('Done');
    console.log(`
Key patterns shown:
  bare(opts)               — zero-defaults agent; you control everything
  compose(a, b, c)         — sequential pipeline; output of each → input of next
  pipe(a).then(b).run(p)   — identical semantics; fluent step-by-step style
  compose(a, b, { when })  — conditional hand-off: only proceed when predicate is true
  compose(a, b, { transform }) — transform output before passing to next agent

Production tip: swap MockLLMProvider for OpenAIProvider or AnthropicProvider.
`);
}

main().catch((e) => {
    console.error(e);
    process.exit(1);
});
